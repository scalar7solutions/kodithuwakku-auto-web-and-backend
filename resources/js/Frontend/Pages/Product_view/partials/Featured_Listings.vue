<script>
import { Link } from '@inertiajs/inertia-vue3'

export default {
  name: 'FeaturedListings',
  components: { Link },
  props: {
    featuredVehicles: {
      type: Array,
      default: () => []
    }
  },
  data() {
    return {
      activeTab: 'inStock',
      page: 0,
      perPageDesktop: 10,
      width: 0        // start at 0, set properly in mounted()
    }
  },
  computed: {
    // switch between 1 per page on phones and 8 per page on desktop
    perPage() {
    if (this.width < 576) {
      // phones
      return 1;
    }
    if (this.width < 1050) {
      // iPad Air / Pro portrait & similar
      return 6;
    }
    if (this.width < 1800) {
      // smaller desktops & tablets landscape
      return 8;
    }
    // ultra-wide screens
    return this.perPageDesktop; // e.g. 10
  },
    // you can filter by tab here if needed
    filtered() {
      return this.featuredVehicles
    },
    // slice out the current page of items
    currentPageItems() {
      const start = this.page * this.perPage
      return this.filtered.slice(start, start + this.perPage)
    }
  },
  methods: {
    switchTab(tab) {
      this.activeTab = tab
      this.page = 0
    },
    prevPage() {
      if (this.page > 0) this.page--
    },
    nextPage() {
      if ((this.page + 1) * this.perPage < this.filtered.length) {
        this.page++
      }
    },
    onResize() {
      this.width = window.innerWidth
      // ensure page reset if perPage changes beneath you
      this.page = 0
    }
  },
  mounted() {
    // set initial width and start listening
    this.onResize()
    window.addEventListener('resize', this.onResize)
  },
  beforeUnmount() {
    // clean up the listener
    window.removeEventListener('resize', this.onResize)
  }
}
</script>


